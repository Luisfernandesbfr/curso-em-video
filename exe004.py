#faça o sucessor , antecessor , o dobro, o triplo e a raiz quadrada de um nuero

n=int(input('Digite um numero: '))
print('O sucessor do numero é ',n+1)
print('O antecessor do numero é ',n-1)
print('O dobro do numero é ',n*2)
print('O triplo do numero é ',n*3)
print('A Raiz Quadrada do numero é ',n**(1/2))

